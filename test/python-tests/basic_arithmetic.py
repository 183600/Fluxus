# Basic arithmetic operations
x = 10
y = 20
sum_result = x + y
diff = y - x
product = x * y
quotient = y // x
remainder = y % x

print("Sum: " + str(sum_result))
print("Difference: " + str(diff))
print("Product: " + str(product))
print("Quotient: " + str(quotient))
print("Remainder: " + str(remainder))